package ch6;

public class Printer {

    static void println(int v){
        System.out.println(v);
    }
    static void println(boolean v){
        System.out.println(v);
    }
    static void println(double v){
        System.out.println(v);
    }
    static void println(String v){
        System.out.println(v);
    }
}
